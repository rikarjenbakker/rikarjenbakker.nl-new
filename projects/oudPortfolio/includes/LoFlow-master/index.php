      What we are currently working on:
    </div>
    <div class="loflow-project__text">
      Currently we are working on the prototype for a smart watermeter that would replace the traditional analog watermeters in the Netherlands. The smart watermeter will use 5G to connect with servers from the local watercompany. This would mean that there is no more need to check the analog meter once a year, or for an employe from a watercompany to collect data using a NFC connection. 
    </div>

